# This is a simple Python script that demonstrates basic output functionality
# It shows how to use the print function to display text to the console

def main():
    # Print a friendly greeting message to the user
    # The print function automatically adds a newline at the end
    print("Hello, World!")

# This is the standard Python idiom for running the main function
# It ensures this code only runs if this file is executed directly (not imported as a module)
if __name__ == "__main__":
    main()